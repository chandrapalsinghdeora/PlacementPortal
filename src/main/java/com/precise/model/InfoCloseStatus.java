package com.precise.model;


public class InfoCloseStatus {

	public int infoCloseStatusId;
	public String clusterName;
	public String cohortName;
	public String compensation;
	public int hires;
	public int noOfApp; 
	public int rollOver; 
	public int verified;
	public int unVerified;
	
	public int getInfoCloseStatusId() {
		return infoCloseStatusId;
	}
	public void setInfoCloseStatusId(int infoCloseStatusId) {
		this.infoCloseStatusId = infoCloseStatusId;
	}
	public String getClusterName() {
		return clusterName;
	}
	public void setClusterName(String clusterName) {
		this.clusterName = clusterName;
	}
	public String getCohortName() {
		return cohortName;
	}
	public void setCohortName(String cohortName) {
		this.cohortName = cohortName;
	}
	public String getCompensation() {
		return compensation;
	}
	public void setCompensation(String compensation) {
		this.compensation = compensation;
	}
	public int getHires() {
		return hires;
	}
	public void setHires(int hires) {
		this.hires = hires;
	}
	public int getNoOfApp() {
		return noOfApp;
	}
	public void setNoOfApp(int noOfApp) {
		this.noOfApp = noOfApp;
	}
	public int getRollOver() {
		return rollOver;
	}
	public void setRollOver(int rollOver) {
		this.rollOver = rollOver;
	}
	public int getVerified() {
		return verified;
	}
	public void setVerified(int verified) {
		this.verified = verified;
	}
	public int getUnVerified() {
		return unVerified;
	}
	public void setUnVerified(int unVerified) {
		this.unVerified = unVerified;
	}
	
}
