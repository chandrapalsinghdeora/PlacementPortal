package com.precise.model;

public class RoleBaseLink {

	public String jdLink;
	public String roleName;
	public String intershipExpLink;
	public String interviewExpLink;
	
	public String getJdLink() {
		return jdLink;
	}
	public void setJdLink(String jdLink) {
		this.jdLink = jdLink;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getIntershipExpLink() {
		return intershipExpLink;
	}
	public void setIntershipExpLink(String intershipExpLink) {
		this.intershipExpLink = intershipExpLink;
	}
	public String getInterviewExpLink() {
		return interviewExpLink;
	}
	public void setInterviewExpLink(String interviewExpLink) {
		this.interviewExpLink = interviewExpLink;
	}
	
}
